package web

import (
	"net/http"

	"github.com/labstack/echo/v4"
)

func  (h *HttpHandler)  InitAllRouter(e *echo.Echo) {
	// freeradius apis
	e.Add(http.MethodPost, "/freeradius/authorize", h.FreeradiusAuthorize)
	e.Add(http.MethodPost, "/freeradius/authenticate", h.FreeradiusAuthenticate)
	e.Add(http.MethodPost, "/freeradius/postauth", h.FreeradiusPostauth)
	e.Add(http.MethodPost, "/freeradius/accounting", h.FreeradiusAccounting)

	// mikrotik cpe query apis
	e.Add(http.MethodGet, "/cpe/mikrotik/device/interfaces", h.QueryMikrotikDeviceInterfaces)
	e.Add(http.MethodGet, "/cpe/mikrotik/device/pppinterfaces", h.QueryMikrotikDevicePPPInterfaces)
	e.Add(http.MethodGet, "/cpe/mikrotik/device/ipinterfaces", h.QueryMikrotikDeviceIpInterfaces)
	e.Add(http.MethodGet, "/cpe/mikrotik/device/routers", h.QueryMikrotikDeviceRouters)
	e.Add(http.MethodGet, "/cpe/mikrotik/device/dns", h.QueryMikrotikDeviceDnsClientServer)

	// Cpe apis
	e.Add(http.MethodPost,  "/cpe/add", h.AddCpe)
	e.Add(http.MethodGet,  "/cpe/query", h.QueryCpe)
	e.Add(http.MethodPost,  "/cpe/update", h.UpdateCpeAttrs)
	e.Add(http.MethodGet,  "/cpe/delete", h.DeleteCpe)
}
